package com.sl.lolsupport.service.dto;

public class SpellData {
	private String spell_key;
	private String img_url;
	private String spell_name;
	
	public String getSpell_key() {
		return spell_key;
	}
	public void setSpell_key(String spell_key) {
		this.spell_key = spell_key;
	}
	public String getImg_url() {
		return img_url;
	}
	public void setImg_url(String img_url) {
		this.img_url = img_url;
	}
	public String getSpell_name() {
		return spell_name;
	}
	public void setSpell_name(String spell_name) {
		this.spell_name = spell_name;
	}
}
